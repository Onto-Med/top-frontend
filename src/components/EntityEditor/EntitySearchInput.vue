<template>
  <div class="row">
    <repository-select-field
      v-if="repositoryFilter"
      v-model="repository"
      :show-details="showDetails"
      :rounded="rounded"
      :outlined="outlined"
      :dense="dense"
      :organisation-id="organisationId"
      :label="t('repository')"
      class="inline col-3 q-mr-xs"
    />
    <q-select
      ref="select"
      v-model="selection"
      :rounded="rounded"
      :outlined="outlined"
      :dense="dense"
      :filled="filled"
      :square="square"
      hide-bottom-space
      hide-dropdown-icon
      use-input
      emit-value
      :autofocus="autofocus"
      class="inline entity-search-input-field col"
      :placeholder="selection ? '' : label || t('selectThing', { thing: t('entity') })"
      :options="options"
      :loading="loading"
      :title="t('entitySearchInput.description', { minLength: minLength, types: t('entity', 2) })"
      :virtual-scroll-item-size="50"
      @filter="filterFn"
      @virtual-scroll="onScroll"
      @update:model-value="handleSelectionChanged"
    >
      <template #append>
        <slot name="append">
          <q-btn
            v-show="!loading"
            round
            dense
            flat
            :icon="icon"
            :title="t('remove')"
            @click="$emit('btnClicked')"
          />
        </slot>
      </template>
      <template #selected>
        <span v-if="selection">
          <q-icon :name="getIcon(selection)" />
          {{ getTitle(selection, true) }}
        </span>
      </template>
      <template #option="scope">
        <q-item v-bind="scope.itemProps">
          <q-item-section avatar>
            <q-icon :name="getIcon(scope.opt)" :title="getIconTooltip(scope.opt)" :class="{ restriction: isRestricted(scope.opt) }" />
          </q-item-section>
          <q-item-section>
            <q-item-label v-if="!(repositoryId && organisationId) && scope.opt.repository" overline>
              {{ scope.opt.repository.name }}
            </q-item-label>
            <q-item-label>{{ getTitle(scope.opt, true) }}</q-item-label>
            <template v-if="showDetails">
              <q-item-label v-for="(description, index) in getDescriptions(scope.opt)" :key="index" caption class="ellipsis">
                {{ description }}
              </q-item-label>
            </template>
          </q-item-section>
          <q-item-section v-show="fork && currentRepositoryId && currentRepositoryId !== scope.opt.repository.id && scope.opt.repository.primary" avatar>
            <q-btn
              v-close-popup
              flat
              round
              icon="content_copy"
              :title="t('forkToCurrentRepository')"
              @click.stop="handleFork(scope.opt)"
            />
          </q-item-section>
        </q-item>
      </template>
      <template #no-option>
        <q-item>
          <q-item-section>
            {{ t('entitySearchInput.emptyResult', { types: t('entity', 2) }) }}
          </q-item-section>
        </q-item>
      </template>
    </q-select>
  </div>
</template>

<script lang="ts">
import { defineComponent, ref, inject, nextTick } from 'vue'
import { useI18n } from 'vue-i18n'
import { EntityType, Entity, Repository, DataType, ItemType, EntityPage } from '@onto-med/top-api'
import { EntityApiKey } from 'boot/axios'
import { AxiosResponse } from 'axios'
import useEntityFormatter from 'src/mixins/useEntityFormatter'
import ScrollDetails from 'src/mixins/ScrollDetails'
import RepositorySelectField from 'src/components/Repository/RepositorySelectField.vue'
import { storeToRefs } from 'pinia'
import { useEntity } from 'src/pinia/entity'
import { QSelect } from 'quasar'
import useNotify from 'src/mixins/useNotify'

export default defineComponent({
  components: {
    RepositorySelectField
  },
  props: {
    label: String,
    minLength: {
      type: Number,
      default: 2
    },
    icon: {
      type: String,
      default: 'close'
    },
    entityTypes: Array as () => EntityType[],
    dataType: String as () => DataType,
    itemType: String as () => ItemType,
    clearOnSelect: Boolean,
    showDetails: Boolean,
    rounded: Boolean,
    outlined: Boolean,
    dense: Boolean,
    filled: Boolean,
    square: Boolean,
    autofocus: Boolean,
    organisationId: String,
    repositoryId: String,
    fork: Boolean,
    repositoryFilter: Boolean
  },
  emits: ['btnClicked', 'entitySelected', 'forkClicked'],
  setup(props, { emit }) {
    const { t } = useI18n()
    const { getTitle, getIcon, getIconTooltip, getSynonyms, getDescriptions, isRestricted } = useEntityFormatter()
    const entityApi  = inject(EntityApiKey)
    const { renderError } = useNotify()
    const options    = ref([] as Entity[])
    const selection  = ref(null)
    const loading    = ref(false)
    const repository = ref(undefined as (Repository|undefined))
    const select     = ref(null as unknown as QSelect)
    const prevInput  = ref(undefined as string|undefined)
    const nextPage   = ref(2)
    const totalPages = ref(0)
    const { repositoryId } = storeToRefs(useEntity())

    const loadOptions = async (input: string|undefined, page = 1): Promise<EntityPage> => {
      if (!entityApi) return Promise.reject({ message: 'Could not load data from the server.' })
      let promise: Promise<AxiosResponse<EntityPage>>
      if (props.organisationId && props.repositoryId) {
        promise = entityApi.getEntitiesByRepositoryId(props.organisationId, props.repositoryId, undefined, input, props.entityTypes, props.dataType, props.itemType, page)
      } else if (repository.value && repository.value.organisation) {
        promise = entityApi.getEntitiesByRepositoryId(repository.value.organisation.id, repository.value.id, undefined, input, props.entityTypes, props.dataType, props.itemType, page)
      } else {
        promise = entityApi.getEntities(undefined, input, props.entityTypes, props.dataType, props.itemType, undefined, true, page)
      }
      return promise.then((r) => r.data)
    }

    return {
      t,
      getTitle,
      getIcon,
      getIconTooltip,
      getSynonyms,
      getDescriptions,
      isRestricted,
      currentRepositoryId: repositoryId,

      select,
      repository,
      options,
      selection,
      loading,

      filterFn (input: string, update: (arg0: () => void) => void, abort: () => void) {
        if (input.length < props.minLength) {
          abort()
          return
        }
        prevInput.value = input
        loading.value = true
        nextPage.value = 2
        totalPages.value = 0
        loadOptions(input)
          .then(page => {
            totalPages.value = page.totalPages
            update(() => options.value = page.content)
          })
          .catch((e: Error) => renderError(e))
          .finally(() => loading.value = false)
      },

      onScroll ({ to, direction, ref }: ScrollDetails) {
        const lastIndex = options.value.length - 1
        if (loading.value || !prevInput.value || nextPage.value > totalPages.value || to !== lastIndex || direction === 'decrease')
          return
        loading.value = true
        loadOptions(prevInput.value, nextPage.value)
          .then(page => {
            totalPages.value = page.totalPages
            if (page.content.length > 0) {
              nextPage.value++
              options.value = options.value.concat(page.content)
              void nextTick(() => {
                ref.refresh()
                loading.value = false
              })
            }
          })
          .catch((e: Error) => renderError(e))
          .finally(() => loading.value = false)
      },

      handleSelectionChanged (entity: Entity) {
        if (props.clearOnSelect) {
          selection.value = null
          repository.value = undefined
        }
        emit('entitySelected', entity)
      },

      handleFork (entity: Entity) {
        select.value?.updateInputValue('')
        emit('forkClicked', entity)
      }
    }
  }
})
</script>
